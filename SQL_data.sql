-- Weather Data Schema
CREATE TABLE IF NOT EXISTS weather_data (
    id SERIAL PRIMARY KEY,
    city VARCHAR(255),
    temperature FLOAT,
    humidity FLOAT,
    recorded_at TIMESTAMPTZ
);

-- AQI Data Schema
CREATE TABLE IF NOT EXISTS aqi_data (
    id SERIAL PRIMARY KEY,
    city VARCHAR(255),
    aqi INT,
    recorded_at TIMESTAMPTZ
);


